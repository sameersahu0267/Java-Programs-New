import java.util.*;

public class LinkHashMap1 {

	public static void main(String[] args) {
		// Linked HashMap in Java
		
		LinkedHashMap<Integer,String> lm = new LinkedHashMap<Integer, String>();
		
		lm.put(101,"Sameer");
		lm.put(102,"Gopal");
		lm.put(103,"Sumeet");
		lm.put(104,"Siddharth");
		
		System.out.println(lm);
		
	}

}
